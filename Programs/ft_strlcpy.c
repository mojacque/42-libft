#include "libft.h"

int		ft_strlcpy(char *restrict dest, const char *restrict src, size_t size)
{
	unsigned int i;

	if (src == NULL)
		return (0);
	if (size == 0)
		return (ft_strlen(src));
	i = 0;
	while (src[i] != '\0' && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (ft_strlen(src));
}
