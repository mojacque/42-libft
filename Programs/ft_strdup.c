#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dest;
	int		lenght;

	i = 0;
	dest = NULL;
	lenght = ft_strlen(src);
	dest = malloc(sizeof(char) * lenght + 1);
	if (dest == NULL)
		return (0);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}